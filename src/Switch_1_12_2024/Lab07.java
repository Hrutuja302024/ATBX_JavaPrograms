package Switch_1_12_2024;

import java.util.Scanner;

public class Lab07 {
    public static void main(String[] args) {
        char code='A';

        switch (code){
            default:
                System.out.println("Helloooo");
                break;
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("90");
                break;
        }
    }
}
