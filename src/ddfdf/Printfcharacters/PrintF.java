package ddfdf.Printfcharacters;

public class PrintF {
    public static void main(String[] args) {
        int a =10;
        System.out.printf("Hi, your value is %d", a);

        String Name="Hrutuja";
        System.out.printf("Hi, your name is %s",Name);

        boolean baby=true;
        System.out.printf("Hi, you have %b",baby);

        long number=3456784567898765L;
        System.out.printf("Hi, your value is %d",number);

    }

}
